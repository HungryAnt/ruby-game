def read_equipments
  equipments = []

  PRICE_CONFIG.split("\n").each do |row|
    fields = row.split(',').map {|f| f.lstrip.rstrip}
    key = fields[0] if fields.size > 0
    price = 1000
    price = fields[1].to_i if fields.size > 1
    equipments << {
        key: key,
        price: price
    }
  end

  equipments
end

def delete_all
  puts 'delete from v1_goods;'
end

def insert_equipment(key, price)
  key_prefix = key.gsub(/(_\d.*|\d.*)$/, '')
  if key.include? 'dragon'
    equipment_type = 'Vehicle'
  elsif key.include? 'pet_'
    equipment_type = 'Pet'
  else
    equipment_type = key_prefix.tr('_', ' ').gsub(/\b\w/) { |first| first.upcase }.tr(' ', '')
  end

  sql = "insert into v1_goods(equipment_type, `key`, price) values('#{equipment_type}', '#{key}', #{price});"
  puts sql
end

def main
  delete_all
  read_equipments.each do |equipment|
    key = equipment[:key]
    price = equipment[:price]
    insert_equipment key, price
  end
end


main
